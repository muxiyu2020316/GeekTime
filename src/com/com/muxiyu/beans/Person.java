package com.com.muxiyu.beans;

/**
 * 描述人的pojo类
 */
public class Person {
    String name;
    Integer age;//property 构造器

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
